def Z_Test(z_score, inverted=False):
    """
    Return the p-value, given a z-score.
    
    If not @inverted, gives the probability region to the left of the x-value.
    If @inverted, gives the probability region to the right of the x-value.
    
    The z-score is rounded to the nearest, lowest 1/100th and a lookup table is
    used, so there may be some very slight inaccuracies, on top of the slight
    inaccuracies inherent in Python.
    
    For z-scores below -3.99 or above 3.99, this simply treats the z-score as
    those numbers respectively.
    
    @z_score
            (float)
            The z-score.
    @inverted
            (bool)
            Whether or not the probability is to the right of the x-value.
    
    Z_Test(float) -> float
    """
    z_score = int(z_score*100)
    if inverted: z_score = -z_score
    if z_score < -399: return 0.00003
    if z_score > 399: return 0.99997
    return Z_TABLE[z_score]

